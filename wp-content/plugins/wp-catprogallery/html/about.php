	<h2><?php _e('short codes'); ?></h2>
	<p>[catpro] - This short code will display all images under all categories which are not disabled.</p>

	<p>[catpro cats=2,3] - This short code will display all images under the categories with ID's 2,3.</p>

	<p>[catpro imgs=1,2,3] - This short code will display images which has ID's 1,2,3.</p>

	<h2><?php _e('short codes to use in template php files'); ?></h2>

